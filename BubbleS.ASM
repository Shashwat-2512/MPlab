.MODEL SMALL
DISPLAY MACRO MSG
        LEA DX, MSG
        MOV AH, 09H
        INT 21H
ENDM
.DATA
LIST DB 02H, 10H, 34H, 0F4H, 09H, 05H
NUMBER EQU $-LIST
MSG1 DB 0DH, 0AH,"1>> SORT IN ASCENDING ORDER$"
MSG2 DB 0DH, 0AH,"2>> SORT IN DESCENDING ORDER$"
MSG2 DB 0DH, 0AH,"3>> EXIT$"
MSG4 DB 0DH, 0AH,"ENTER YOUR CHOICE ENTERED...$"
MSG5 DB 0DH, 0AH,"INVALID CHOICE ENTERED$"
.CODE
START: MOV AX,@DATA
       MOV DS,AX
       LEA SI,LIST
       MOV CH, NUMBER-1
       DISPLAY MSG1
       DISPLAY MSG2
       DISPLAY MSG3
       DISPLAY MSG4
       MOV AH, 01H
       INT 21H
       SUB AL, 30H
       CMP AL, 01H
       JE ASCSORT
       CMP AL,02H
       JE DECSORT
       CMP AL, 03H
       JE FINAL
       DISPLAY MSGS
       JMP FINAL
ASCSORT:MOV BL, 00H
AGAIN: MOV SI, OFFSET LIST
       MOV CL, 00H
       MOV BH, CH
       SUB BH, BL
NPASS: CMP CL, BH
       JNC NEXT
       MOV AL,[SI]
       MOV BP, 01H
       CMP AL, DS:[BP][SI]
       JC_NOPE
       XCHG AL, [SI+1]
       XCHG [SI],AL
_NOPE: INC SI
       JMP NPASS
NEXT:  INC BL
       CMP BL,CH
       JC AGAIN
       JMP FINAL
DECSORT:MOV BL, 00H
        AGAIN1:MOV SI, OFFSET LIST
        MOV CL,00H
        MOV BG,CH
        SUB BH,BL
NPASS1: CMP CL,BH
        JNC NEXT
        MOV AL,[SI]
        MOV BP,01H
        CMP AL,DS:[BP][SI]
        JNC_NOPE1
        XCHG AL,[SI+1]
        XCHG [SI],AL
_NOPE1: INC CL
        INC SI
        JMP NPASS1
NEXT1:  INC BL
        CMP BL, CH
        JC AGAIN1
FINAL:  MOV AH, 4CH
        INT 21H
END START

